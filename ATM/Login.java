    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        try {
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/ATM", "root", "root");
            String q = "SELECT * FROM account";
            
            phone = phone_text.getText();
            pin = Integer.parseInt(new String(pin_text.getPassword()));

            smnt = con.createStatement();
            set = smnt.executeQuery(q);

            while (set.next()) {
                String phoneno = set.getString(3);
                int atmpin = set.getInt(4);
                if (phone.equals(phoneno) && atmpin == pin) {
                    JOptionPane.showMessageDialog(null, "User Login Succcessful.");
                    Menu mnu = new Menu();
                    mnu.setPhone(phone);
                    this.setVisible(false);
                    mnu.setVisible(true);
                    
                    break;
                }
            }
            
            phone_text.setText("");
            pin_text.setText("");
            
            set.close();
            smnt.close();
            con.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_submitButtonActionPerformed

