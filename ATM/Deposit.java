    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        try {
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/ATM", "root", "root");
            String query = "UPDATE account SET balance=? WHERE phoneno=?";
            pst = con.prepareStatement(query);  
            
            amount = Integer.parseInt(amount_text.getText());
            int bal = amount + balance;

            pst.setInt(1, bal);
            pst.setString(2, phone_no);

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Amount Added Successfully");

            pst.close();
            con.close();
        } catch (SQLException ex) {
            System.out.println(ex);
        }
        amount_text.setText("");
    }//GEN-LAST:event_submitButtonActionPerformed

